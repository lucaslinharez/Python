# Concatenação em Python, é o processo de juntar ou combinar valores
# Geralmente entre strs, em uma única sequência. A forma mais comum é usar o operador +

# Exemplo de concatenação

concatenacao = 'Texto' + 'Texto'  # somente strings
print(concatenacao)

# Exemplo de repetição, onde o operador * é usado

teste1 = 'A' * 5
teste2 = '3 vezes' * 3            # numeros inteiros
print(teste1)
print(teste2)

