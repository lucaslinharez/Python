""" A função print, recebe argumentos e mostra na tela"""

print(1,2,3)

""" Como padrão da função, o espaço já é inserido, mas, podemos usar sep= para escolher outro tipo de separador"""

print(1,2,3, sep="-")

""" Para quebra de linha ao final do código, usamos end=\n"""

print(10, 9, 5, sep='-', end='\n123')

"""As funções funcionam apenas com numeros"""
